import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outputPath = path.join(root, 'src', 'generated', 'question-banks.js');
const sources = ['math', 'english', 'science'].flatMap(subject => [
  { file: `${subject}.json`, subject, mode: 'normal' },
  { file: `hard-${subject}.json`, subject, mode: 'hard' },
]);

const entries = await Promise.all(
  sources.map(async source => {
    const contents = await readFile(path.join(root, 'data', source.file), 'utf8');
    const bank = JSON.parse(contents);
    if (
      bank.schemaVersion !== 1 ||
      bank.subject !== source.subject ||
      bank.mode !== source.mode
    ) {
      throw new Error(`${source.file}: unexpected question-bank metadata`);
    }
    return [`${source.subject}:${source.mode}`, bank];
  })
);

const payload = JSON.stringify(Object.fromEntries(entries)).replaceAll('<', '\\u003c');
const output = [
  '// Generated by tools/build-standalone-banks.mjs. Do not edit directly.',
  `globalThis.__NAMELESS_QUESTION_BANKS__=${payload};`,
  '',
].join('\n');

if (process.argv.includes('--check')) {
  const existing = await readFile(outputPath, 'utf8').catch(() => '');
  if (existing !== output) {
    console.error('Standalone question-bank bundle is missing or stale.');
    process.exitCode = 1;
  } else {
    console.log(`Standalone question-bank bundle is current (${payload.length} bytes).`);
  }
} else {
  await writeFile(outputPath, output, 'utf8');
  console.log(`Built standalone question-bank bundle (${payload.length} bytes).`);
}
